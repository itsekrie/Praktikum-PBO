 public class Person{
	 private int id;
	 private String name;
	 
	 public Person(String n){
		 name = name;
	 }
	 public Person(int i, String n){
		 id = i;
		 name = n;
	 }
	 
	 public int getId(){
		 return id;
	 }
	 
	 public String getName(){
		 return name;
	 }
 }
